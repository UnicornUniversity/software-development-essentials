var x = 1;

if (x == 1){
    console.log("x is 1");
}
else{
    console.log("x is not 1");
}

switch(x){
    case 1:
        console.log("x is 1");
        break;
    case 2:
        console.log("x is 2. How can it be?!");
        break;
    default:
        console.log("x is something else...");
        break;
}

